# Texto (string)

comillasSimples = 'Este es un texto'
comillasDobles = "Este es otro texto"
comillasTriples = '''Este es un texto con comillas triples'''

print(comillasSimples)
print(comillasDobles)
print(comillasTriples)

# Números

a = 1
b = 3.14
c = 5 + 2j

print(a)
print(b)
print(c)

# lista

lista = [0, 1, 2, 3, 4, 5]

print(lista)

# tupla

tupla = ("a", "b", "c")
print(tupla)    

# diccionario

diccionario = {
    "nombre": "Juan", 
    "edad": 30, 
    "ciudad": "Madrid"
    }
print(diccionario)

# Conjunto (set)
conjunto = {1, 1, 2, 2 , 3} # Output: {1, 2, 3}

print(conjunto)

